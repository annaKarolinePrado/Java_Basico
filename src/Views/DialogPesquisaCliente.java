    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try{        
            LinkedList<Cliente> listaTodosClientes  = Dados.DadosCliente.getCliente();
            int filtro = textCodigo.getText().trim().isEmpty() ? 0 :Integer.parseInt(textCodigo.getText());        
            //pesquisar por codigo
            LinkedList<Cliente> filtraPorCodigos = new LinkedList<>();
            for (Cliente listaTodosCliente : listaTodosClientes) {
                if(!textCodigo.getText().trim().isEmpty()){                
                    if(listaTodosCliente.getId() == filtro){
                        filtraPorCodigos.add(listaTodosCliente);
                    }
                }else{
                    filtraPorCodigos.add(listaTodosCliente); 
                }
            }
            //filtra por nome
            LinkedList<Cliente> filtraPorNomes = new LinkedList<>();
            for (Cliente filtraPorCodigo : filtraPorCodigos) {
                if(!textNome.getText().trim().isEmpty()){                
                    if(filtraPorCodigo.getNome().contains(textNome.getText().toUpperCase())){
                        System.out.println(filtraPorCodigo.getNome()+"teste");
                        filtraPorNomes.add(filtraPorCodigo); 
                    }
                }else{
                    filtraPorNomes.add(filtraPorCodigo); 
                }
            }
            
            areaTexto.setText("");
            for (Cliente cliente : filtraPorNomes) {
                areaTexto.append(cliente.getId()+ "\t" +cliente.getNome() + "\n");
            }
        }catch(NumberFormatException ex){
            System.out.println("Erro ao digitar o código." + ex.getMessage());
        }        
    }//GEN-LAST:event_btnPesquisarActionPerformed

