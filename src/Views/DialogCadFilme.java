    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        
        if(textNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Nome obrigatório.");
            textNome.requestFocus();
            return;
        }
        if(textDtLanc.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(null, "Campo Dt. Lançamento obrigatório.");
           textDtLanc.requestFocus();
           return;
        }
        if(textQuant.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(null, "Campo Quantidade obrigatório.");
           textQuant.requestFocus();
           return;
        }
        if(textSinopse.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(null, "Campo Snopse obrigatório.");
           textSinopse.requestFocus();
           return;
        }
      
        String nome         = textNome.getText();
        String snopse       = textSinopse.getText();
        String data_lanc    = textDtLanc.getText();
        int quant           = Integer.parseInt(textQuant.getText());
        
        int id = 0;        
        //buscar o maior id
        for (Filme filme :  Dados.DadosFilme.getFilme()) {
            id = Math.max(id, filme.getId());
        }
        
        Categoria cat = new Categoria();        
        for (Categoria categoria : DadosCategoria.getCategoria()) {
            if (categoria.getNome() == comboCategoria.getSelectedItem()){
                cat = categoria;
            }            
        }    
        
        Filme filme = new Filme(id+1, nome, snopse, data_lanc, quant, cat);
        
        Dados.DadosFilme.getFilme().add(filme);
        
        textId.setText("");
        textNome.setText("");
        textDtLanc.setText("");
        textQuant.setText("");
        textDtLanc.setText("");
        textSinopse.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

