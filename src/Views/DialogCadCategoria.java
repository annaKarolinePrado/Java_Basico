    private void btnCadastraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastraActionPerformed
        // TODO add your handling code here:
        if (textNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Nome obrigatório");
            return;
        }
        int id = textId.getText().isEmpty()? 0: Integer.parseInt(textId.getText());
        String nome = textNome.getText();
        String status = radioAtivo.isSelected()?"ATIVO":"INATIVO";
        
        Categoria categoria = new Categoria(id, nome, status);
        
        Dados.DadosCategoria.getCategoria().add(categoria);
            
    }//GEN-LAST:event_btnCadastraActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        areaTexto.setText("");
        areaTexto.append("Código\tNome\tStatus\n");
        for (Categoria categoria : Dados.DadosCategoria.getCategoria()) {
            
            areaTexto.append(categoria.getId() + "\t" + categoria.getNome() + "\t" + categoria.getStatus() + "\n");
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

