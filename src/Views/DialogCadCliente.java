    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if(textId.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo ID obrigatorio.");
            textId.requestFocus();
            return;
        }        
        if(textNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo nome obrigatorio");
            textNome.requestFocus();
            return;
        }
        if(textDtNasc.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Dt. nascimento obrigatorio");
            textDtNasc.requestFocus();
            return;
        }
        if(textCPF.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo CPF obrigatorio");
            textCPF.requestFocus();
            return;
        }
        if(textTelefone.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo telefone obrigatorio");
            textTelefone.requestFocus();
            return;
        }
        if(textEmail.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo e-mail obrigatorio");
            textEmail.requestFocus();
            return;
        }
        if(textEndereco.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo endere√ßo obrigatorio");
            textEndereco.requestFocus();
            return;
        }
        
        int id = Integer.parseInt(textId.getText());
        String nome = textNome.getText();
        String cpf = textCPF.getText();
        String data_nasc = textDtNasc.getText();
        String telefone = textTelefone.getText();
        String endereco = textEndereco.getText();
        String email = textEmail.getText();
        
        Cliente cliente = new Cliente(id, nome, cpf, data_nasc, telefone, endereco, email);
        
        Dados.DadosCliente.getCliente().add(cliente);
        textId.setText("");
        textNome.setText("");
        textCPF.setText("");
        textDtNasc.setText("");
        textTelefone.setText("");
        textEmail.setText("");
        textEndereco.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        
        
        areaTexto.setText("");
        areaTexto.append("C√≥digo\tNome\t\tCPF\tTelefone\n");
        for (Cliente cliente : Dados.DadosCliente.getCliente()) {
            
            areaTexto.append(cliente.getId() +"\t"+ cliente.getNome() +"\t"+ cliente.getCpf() +"\t"+ cliente.getTelefone() + "\n");
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

