    private void btnCadastraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastraActionPerformed
        
        if(textNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Nome obrigatorio.");
            textNome.requestFocus();
            return;
        }
        if(textCpf.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo CPF obrigatorio.");
            textCpf.requestFocus();
            return;
        }
        if(textDtNasc.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Dt. Nasc. obrigatorio.");
            textDtNasc.requestFocus();
            return;
        }
        if(textTelefone.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Telefone obrigatorio.");
            textTelefone.requestFocus();
            return;
        }
        if(textEmail.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo E-mail obrigatorio.");
            textEmail.requestFocus();
            return;
        }
        if(textEndereco.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Endereço obrigatorio.");
            textEndereco.requestFocus();
            return;
        }
     
        String nome     = textNome.getText();
        String cpf      = textCpf.getText();
        String dtNasc   = textDtNasc.getText();
        String telefone = textTelefone.getText();
        String email    = textEmail.getText();
        String endereco = textEndereco.getText();       
        String funcao   = comboFuncao.getSelectedItem()+"";
        
        int id = 0;        
        //buscar o maior id
        for (Funcionario funcionario :  Dados.DadosFuncionario.getFuncionario()) {
            id = Math.max(id, funcionario.getId());
        }
        
        Funcionario funcionario = new Funcionario(id+1, nome, cpf, funcao, dtNasc, telefone, email, endereco );
        
        Dados.DadosFuncionario.getFuncionario().add(funcionario);
        
        textId.setText("");
        textNome.setText("");
        textCpf.setText("");
        textTelefone.setText("");
        textDtNasc.setText("");
        textEmail.setText("");
        textEndereco.setText("");
    }//GEN-LAST:event_btnCadastraActionPerformed

    private void btnMostraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostraActionPerformed
        areaTexto.setText("");
        
        areaTexto.append("ID\tNome\t\tFunção\t\tTelefone\n");
        for (Funcionario funcionario : Dados.DadosFuncionario.getFuncionario()) {
            areaTexto.append(funcionario.getId() + "\t" + funcionario.getNome() + "\t" + funcionario.getFuncao()+ "\t" +
                             funcionario.getTelefone() + "\n" );
        }
        
    }//GEN-LAST:event_btnMostraActionPerformed

    private void textEditarMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textEditarMouseExited
        
        try {
            int codigo = Integer.parseInt(textEditar.getText());
            for (Funcionario funcionario : Dados.DadosFuncionario.getFuncionario()) {
                if(funcionario.getId() == codigo){
                    textId.setText(funcionario.getId() + "");
                    textNome.setText(funcionario.getNome());
                    textCpf.setText(funcionario.getCpf());
                    textDtNasc.setText(funcionario.getData_nasc());
                    textEmail.setText(funcionario.getEmail());
                    textEndereco.setText(funcionario.getEndereco());
                    textTelefone.setText(funcionario.getTelefone());
                    comboFuncao.setSelectedItem(funcionario.getFuncao());
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null,"Código invalido.");
            textEditar.requestFocus();
            return;
        }
    }//GEN-LAST:event_textEditarMouseExited

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        try {
            int id = Integer.parseInt(textId.getText());
            if(textId.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Informe um código para edição");
                textEditar.requestFocus();
                return;
            }else{
                for (Funcionario funcionario : Dados.DadosFuncionario.getFuncionario()) {
                    if(funcionario.getId()== id){
                        funcionario.setId(id);
                        funcionario.setNome(textNome.getText());
                        funcionario.setCpf(textCpf.getText());
                        funcionario.setData_nasc(textDtNasc.getText());
                        funcionario.setEmail(textEmail.getText());
                        funcionario.setEndereco(textEndereco.getText());
                        funcionario.setTelefone(textTelefone.getText());
                        funcionario.setFuncao(comboFuncao.getSelectedItem() + "");
                    }
                }
            }
            textId.setText("");
            textNome.setText("");
            textCpf.setText("");
            textDtNasc.setText("");
            textTelefone.setText("");
            textEmail.setText("");
            textEndereco.setText("");
            textEditar.setText("");
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null,"Código invalido.");
            textEditar.requestFocus();
            return;
        }
    }//GEN-LAST:event_btnEditarActionPerformed

